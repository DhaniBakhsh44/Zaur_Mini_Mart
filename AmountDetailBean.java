public class AmountDetailBean {
     private int amount_id;
   private int user_id;
   private int user_selling_amount;
   private String date;

    public int getAmount_id() {
        return amount_id;
    }

    public void setAmount_id(int amount_id) {
        this.amount_id = amount_id;
    }

    public int getUser_id() {
        return user_id;
    }

    public void setUser_id(int user_id) {
        this.user_id = user_id;
    }

    public int getUser_selling_amount() {
        return user_selling_amount;
    }

    public void setUser_selling_amount(int user_selling_amount) {
        this.user_selling_amount = user_selling_amount;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }
    
  
    
}
