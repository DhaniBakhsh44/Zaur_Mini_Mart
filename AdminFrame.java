private void addItems(int rows){
 DateFormat df = new SimpleDateFormat("YY-MM-dd");
Calendar calobj = Calendar.getInstance();
String date=(String)df.format(calobj.getTime());
System.out.println(date);

try {
      Class.forName("com.mysql.jdbc.Driver");
    Connection   con=DriverManager.getConnection("jdbc:mysql://localhost:3306/mini_mart","root","");
  
    
    //  con.setAutoCommit(false);
    String query = "Insert into products (prod_id, prod_name, prod_barcode, prod_qty, prod_price, date) values (?,?,?,?,?,?)";
    PreparedStatement pst = con.prepareStatement(query);

    for (int row = 0; row <rows; row++) {
            String prod_id=(String)itemTable.getValueAt(row,0);
    if(prod_id==null){
        String prod_name = (String) itemTable.getValueAt(row, 1);
        String barcode = (String) itemTable.getValueAt(row, 2);   
        int quantity =Integer.parseInt(itemTable.getValueAt(row, 3).toString());
        int unit_price =Integer.parseInt(itemTable.getValueAt(row, 4).toString());
        
        pst.setInt(1,0);   
        pst.setString(2, prod_name);
        pst.setString(3, barcode);
        pst.setInt(4, quantity);
        pst.setInt(5, unit_price);
        pst.setString(6, date);
        pst.addBatch();
    }// end of if
    }// end of loop
    pst.executeBatch();
JOptionPane.showMessageDialog(this,"ITEMS INSERT SUCEFULLY....");

    //con.commit();
}catch(NumberFormatException ne){
 JOptionPane.showMessageDialog(null,"Kindly Insert Number in Qty And Unit Price Column..");   
}catch (NullPointerException e) {
    
    JOptionPane.showMessageDialog(this,"Kindly Detele Empty Rows..");

}catch (Exception e) {
    e.printStackTrace();
    JOptionPane.showMessageDialog(this,"Error"+e);

}

 }
    
