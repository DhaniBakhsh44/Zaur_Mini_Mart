private void getDataByBarcode(String barcode){
   
        try{
           
            Vector v=DBManager.getDataByBarcode(barcode);
            if(v.isEmpty()){
               int prod_id=Integer.parseInt(barcode);
               v=DBManager.getDataByProdId(prod_id);
           }
            
            System.out.println(v.size());
           if(v.size()==0){
               JOptionPane.showMessageDialog(this,"Item Not Found...");
               return;
           }
            DefaultTableModel dm = (DefaultTableModel)itemTable.getModel();
            for(int i=0; i<v.size(); i++){
               ProductsBean bean=(ProductsBean)v.elementAt(i);
               int prod_id=bean.getProd_id();
               String prod_name=bean.getProd_name();
               String prod_barcode=bean.getProd_barcode();
               int prod_qty=bean.getProd_qty();
             //  int prod_price=bean;
               dm.addRow(new Object[]{bean.getProd_id(),bean.getProd_name(),bean.getProd_barcode(),bean.getProd_qty(), bean.getProd_price()}); 
  
            }
            
            
        }catch(Exception e){
            e.printStackTrace();
            JOptionPane.showMessageDialog(this,"Exception"+e);
        }
   
}
    
